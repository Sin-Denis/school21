int		ft_iterative_factorial(int nb)
{
	int	res;

	res = 1;
	if (nb >= 13 || nb < 0)
	{
		return (0);
	}
	while (nb)
	{
		res *= nb;
		nb -= 1;
	}
	return (res);
}
